    <div class="materi-section">
        <span class="badge badge-topik rounded-pill mb-2">Bagian 1: Dunia Digital & Netiket</span>
        <h2 class="fw-bold text-dark mb-4">A. Berkomunikasi di Dunia Maya dengan Etika</h2>
        
        <p>
            Saat kita memasuki dunia internet, kita secara otomatis menjadi bagian dari <strong>Warga Digital (Cyber Citizen)</strong>. Sama seperti di dunia nyata yang memiliki norma kesopanan dan adat istiadat, dunia maya juga menuntut warganya untuk memiliki etika khusus yang dikenal dengan istilah <strong>Netiket (Network Etiquette / Etika Berinternet)</strong>.
        </p>
        <p>
            Banyak orang sering lupa bahwa di balik layar monitor atau *smartphone* yang mereka tatap, ada manusia nyata yang memiliki perasaan. Oleh karena itu, aturan utama dari Netiket adalah <em>"Ingatlah orang di balik layar"</em>. Jangan menuliskan sesuatu di media sosial yang sekiranya tidak berani kamu ucapkan secara langsung secara tatap muka di dunia nyata.
        </p>

        <h4 class="fw-bold text-primary mt-4">Poin Utama Pelanggaran Netiket yang Sering Terjadi:</h4>
        <ul>
            <li><strong>Cyberbullying (Perundungan Siber):</strong> Tindakan mengejek, mengintimidasi, mempermalukan, atau mengucilkan seseorang menggunakan media digital (seperti di kolom komentar Instagram, TikTok, atau grup WhatsApp). Perundungan di dunia maya sering kali berdampak lebih fatal pada mental korban karena jejaknya bisa dilihat oleh ribuan orang secara terus-menerus.</li>
            <li><strong>Penyebaran Berita Bohong (Hoax):</strong> Membagikan informasi atau berita yang belum jelas kebenarannya tanpa melakukan *check and recheck* terlebih dahulu. Motif penyebaran hoax bisa bermacam-macam, mulai dari sekadar iseng, mencari klik/views (*clickbait*), hingga upaya adu domba.</li>
        </ul>
    </div>

    <div class="materi-section" style="border-left-color: #06b6d4;">
        <span class="badge badge-topik rounded-pill mb-2" style="background-color: #ecfeff; color: #0891b2;">Bagian 2: Privasi & Keamanan</span>
        <h2 class="fw-bold text-dark mb-4">B. Perlindungan Privasi dan Bahaya Kejahatan Siber</h2>
        
        <p>
            Di era big data, data pribadi kita adalah komoditas yang sangat berharga dan rawan disalahgunakan oleh pihak yang tidak bertanggung jawab. Data pribadi dibagi menjadi dua, yaitu data umum (seperti nama lengkap, jenis kelamin) dan data spesifik yang **wajib dilindungi ketat** (seperti NIK KTP, nomor rekening bank, isi rekam medis, dan password akun pribadi).
        </p>

        <h4 class="fw-bold text-info mt-4">Ancaman Keamanan Siber yang Mengintai:</h4>
        <ul>
            <li><strong>Phishing:</strong> Teknik penipuan siber yang memancing korban agar memberikan data sensitif (seperti username dan password) dengan cara menyamar sebagai instansi resmi melalui email palsu, tautan WhatsApp bodong, atau situs web tiruan yang mirip aslinya.</li>
            <li><strong>Jejak Digital (Digital Footprint):</strong> Segala rekam jejak aktivitas kita saat menjelajahi internet, mulai dari riwayat pencarian, foto yang diunggah, hingga komentar yang pernah ditulis. Ingat, jejak digital sangat sulit dihapus sepenuhnya dan sering kali digunakan oleh perusahaan atau universitas untuk menyeleksi calon mahasiswa atau karyawan di masa depan.</li>
        </ul>
    </div>

    <div class="materi-section" style="border-left-color: #e11d48;">
        <span class="badge badge-topik rounded-pill mb-2" style="background-color: #fff1f2; color: #be123c;">Bagian 3: Hukum & Legalitas</span>
        <h2 class="fw-bold text-dark mb-4">C. Aspek Hukum Teknologi: Mengenal UU ITE & HAKI</h2>
        
        <p>
            Kebebasan berpendapat di internet Indonesia dibatasi oleh koridor hukum yang tegas, yaitu <strong>UU ITE (Undang-Undang Informasi dan Transaksi Elektronik)</strong>. UU ini dibuat untuk melindungi pengguna internet sekaligus menindak tegas para pelaku kejahatan digital di Indonesia.
        </p>

        <div class="highlight-box">
            <h5 class="fw-bold text-danger">⚠️ Pasal Krusial UU ITE yang Wajib Diketahui Siswa SMA:</h5>
            <p>
                Melalui undang-undang ini, menyebarkan ujaran kebencian, melakukan penghinaan/pencemaran nama baik orang lain secara online, menyebarkan konten pornografi, serta membagikan berita hoax yang merugikan konsumen dapat dikenakan sanksi pidana berupa denda ratusan juta rupiah hingga hukuman penjara. Oleh karena itu, kita harus bijak berpikir sebelum mengetik (*Think before you post*).
            </p>
        </div>

        <h4 class="fw-bold text-danger mt-4">Hak Kekayaan Intelektual (HAKI) & Plagiarisme</h4>
        <p>
            Selain UU ITE, kita juga harus menghormati karya orang lain di internet. <strong>HAKI</strong> melindungi hak cipta berupa gambar, video, musik, teks, tulisan, dan software yang diunggah orang lain di internet. 
        </p>
        <p>
            Mengambil, memakai, atau menyalin karya orang lain lalu mengeklaimnya sebagai karya buatan kita sendiri tanpa mencantumkan sumber asli disebut tindakan <strong>Plagiarisme</strong>. Di dunia akademik informatika, plagiarisme adalah pelanggaran berat terhadap kode etik dan moralitas digital.
        </p>
    </div>

